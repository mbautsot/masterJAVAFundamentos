package FlujoDeControl;

public class SentenciasIfElse {
    public static void main(String[] args) {

        /*
            TODO
                En Java, if y else son estructuras de control de flujo que permiten tomar decisiones en base a condiciones específicas.
                Aquí te explico cada una:
               * if se utiliza para ejecutar un bloque de código si una condición especificada es verdadera.
               * else se utiliza junto con if para ejecutar un bloque de código alternativo si la condición especificada en el if es falsa.
               * else if: Además de if y else, se puede usar else if para verificar múltiples condiciones en secuencia.
         */

        double promedio = 8.5d;

        if (promedio>8){
            System.out.println("Has aprobado Felicidades");
        }else{
            System.out.println("No has aprobado.");
        }
    }
}
